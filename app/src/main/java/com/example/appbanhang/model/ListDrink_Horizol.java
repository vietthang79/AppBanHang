package com.example.appbanhang.model;

public class ListDrink_Horizol {
    int img;
    String name;

    public ListDrink_Horizol() {
    }

    public ListDrink_Horizol(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
